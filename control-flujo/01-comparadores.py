'''
Comparadores Logicos

             <
             >
             <=
             >=
             ==
             !=
'''
#1 es mayor a 2
print(1 > 2)

#1 es meor a 2
print(1 < 2)

#1 es menor o igual a 2
print(1 <= 2)

#1 es mayor o igual a 2
print(1 >= 2)

#2 es igual a 2
print(2 == 2)

#1 es diferente de 2
print(1 != 2)
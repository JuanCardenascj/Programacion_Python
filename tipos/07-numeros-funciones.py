import math #Codigo escrito en python

#Ejemplo
#Devuelve al numero al que se encuentre mas cercano
print(round(1.3)) 
print(round(1.5)) 
print(round(1.7)) 

#Saca el signo positivo de la ecuacion
print(abs(-77))

#Ejemplos del MATH
print(math.ceil(1.1)) #Numero superior entero mas cercano hacia arriba
print(math.floor(1.9999)) #Toma el numero y lo lleva al numero entero mas cercano hacia abajo
print(math.isnan(23)) #Lo que escribe en el parentesis no es un numero
print(math.pow(10, 3)) #Permite elevar un numero a la potencia 
print(math.sqrt(9)) #Sacar la raiz cuadrada del numero
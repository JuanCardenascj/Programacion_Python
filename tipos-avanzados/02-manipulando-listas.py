#Ejemplo lista de mascotas
mascotas = ["Wofl", "Pelusa", "Simon", "Pulga"]
print(mascotas[0])
#para cambiar un elemento del listado
mascotas[0] = "Bicho"
print(mascotas) #Muestra el elemento modifcado en la lista
#Obtencion parcial de la lista - una parte
print(mascotas[0:3]) #Nos muestra los primeros 3 elementos
#Saltar entre la lista
print(mascotas[::2]) #toma el primer elemento, salta el siguiente y toma el que le sigue al siguiente

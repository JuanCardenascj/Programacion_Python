'''
Operadores Aritmeticos
'''

#Suma - Podemos sumar directamente los valores
resultado = 10 + 5
print(resultado)

#Suma - Tambien podemos sumarlos por medio de variables
num1 = 10
num2 = 5

resultado = num1 + num2
print(resultado)

#Resta - Podemos resta directamente los valores
resultado = 10 - 5
print(resultado)

#Resta - Tambien podemos restarlo por medio de variables
num1 = 10
num2 = 5

resultado = num1 - num2
print(resultado)

#Multiplicacion - Podemos multiplicacion directamente los valores
resultado = 10 * 5
print(resultado)

#Multiplicacion - Tambien podemos rmultiplicarlo por medio de variables
num1 = 10
num2 = 5

resultado = num1 * num2
print(resultado)

#Division - Podemos divison directamente los valores
resultado = 10 / 5
print(resultado)

#Division - Tambien podemos rdividirlo por medio de variables
num1 = 10
num2 = 5

resultado = num1 / num2
print(resultado)


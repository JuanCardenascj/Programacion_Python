'''
Diccionarios : Tienen  dos elementos por posicion y se guardan de manera desordenada
'''

#Ejemplo

diccionario = {"azul":"blue", 
               "rojo":"red",
               "verde":"green"} #Clave : Valor, y eso ya seri un elemento

diccionario["amarillo"] = "yellow" #Se Agraga elementos la diccionario
del(diccionario["amarillo"]) #Para eliminar un valor del diccionario


print(diccionario)
print(diccionario["rojo"]) #Para saber como se dice tal color en su traduccion a ingles
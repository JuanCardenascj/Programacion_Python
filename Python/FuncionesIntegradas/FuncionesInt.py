'''
Funciones Integradas
'''
#Ejemplos

#Conversion de un numerico a una cadena
n = str(10) #Entero

print(n)

#Conversion de un numerico a una cadena
n = str(10.98) #Float

print(n)

#Conversion de un entero a un binario
n = bin(10) #Binario

print(n)

#Conversion de un entero a un hexadecimal
n = hex(10) #Hexadecimal

print(n)

#Conversion de un binario a un entero
n = int("0b1010",2) #Binario, entero

print(n)

#Conversion de un hexagecimal a un entero
n = int("0xa", 16) #Hexagecimal, entero

print(n)

'''Como sacar el valor absoluto de un numero
 El valor absoluto es la distancia del numero hacia el 0 pero en positivo
'''
#Valor absoluto
n = abs(-8)

print(n)

#Valor absoluto - Redondea un numero
n = round(5.6)

print(n)

#Valor absoluto - Se le pasa argumento una cadena
n = len("JuanDavid") #Cuenta cuantos caracteres existen

print(n)

